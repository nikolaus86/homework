def get_matrix(n, m, value):
    # Проверяем значения параметров n и m
    if n <= 0 or m <= 0:
        return []

    matrix = []  # Создаем пустой список для матрицы

    for i in range(n):  # Внешний цикл - количество строк
        row = []  # Создаем пустой список для строки
        for j in range(m):  # Внутренний цикл - количество столбцов
            row.append(value)  # Заполняем строку значением
        matrix.append(row)  # Добавляем строку в матрицу

    return matrix  # Возвращаем заполненную матрицу

# Примеры использования функции
result1 = get_matrix(2, 2, 10)
result2 = get_matrix(3, 5, 42)
result3 = get_matrix(4, 2, 13)

# Вывод результатов
print(result1)  # [[10, 10], [10, 10]]
print(result2)  # [[42, 42, 42, 42, 42], [42, 42, 42, 42, 42], [42, 42, 42, 42, 42]]
print(result3)  # [[13, 13], [13, 13], [13, 13], [13, 13]]
