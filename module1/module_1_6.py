
# Создание словаря
my_dict = { "Алексей": 1990,   "Мария": 1985,   "Дмитрий": 1992}

# Вывод словаря на экран
print("Словарь my_dict:", my_dict)

# Вывод значения по существующему ключу
print("Год рождения Алексея:", my_dict["Алексей"])

# Попытка вывести значение по отсутствующему ключу без ошибки
missing_key = my_dict.get("Петр", "Ключ 'Петр' отсутствует в словаре")
print(missing_key)

# Добавление двух произвольных пар
my_dict["Ольга"] = 1988
my_dict["Иван"] = 1995

# Удаление одной пары по существующему ключу и вывод её значения
removed_value = my_dict.pop("Мария")
print("Удаленный год рождения Марии:", removed_value)

# Вывод измененного словаря на экран
print("Обновленный словарь my_dict:", my_dict)

# Работа с множествами
# Создание множества с различными типами данных
my_set = {1, 3.14, "Привет", True, 1, 3.14}  # Пробуем добавить дубликаты

# Вывод множества на экран
print("Множество my_set:", my_set)

# Добавление 2 произвольных элементов
my_set.add("Мир")
my_set.add(42)

# Удаление одного элемента из множества
my_set.discard(1)  # Используем discard для безопасного удаления

# Вывод измененного множества на экран
print("Измененное множество my_set:", my_set)
